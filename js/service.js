function toggleVisibilityServicesFirst() {
    var services_toggle_first = document.getElementById("services_toggle_first");
    var services_arrow_first = document.getElementById("services_arrow_first")

    if(services_toggle_first.style.display === "none") {
        services_toggle_first.style.display = "block"
        services_arrow_first.style.transform = "rotate(-45deg)"
    } else {
        services_toggle_first.style.display = "none"
        services_arrow_first.style.transform = "rotate(135deg)"
    }
}

